@data  = nil
@xsize = nil
@ysize = nil

puts "\n\n.   .   start   .   .".colorize(mode: :bold, color: :white)

def copy_data data
  @data = data
end

def find_dimensions
  @xsize = @data[0].size
  @ysize = 0
  @data.each { @ysize += 1 }
end

def is_roll_at? x, y
  return false if x < 0
  return false if y < 0
  begin
    return @data[y][x] == '@'
  rescue
    return false
  end
end

def is_roll? pos, x, y
  case pos
  when :up_left     then is_roll_at? x - 1, y - 1
  when :up_centre   then is_roll_at? x    , y - 1
  when :up_right    then is_roll_at? x + 1, y - 1
  when :left        then is_roll_at? x - 1, y
  when :right       then is_roll_at? x + 1, y
  when :dn_left     then is_roll_at? x - 1, y + 1
  when :dn_centre   then is_roll_at? x    , y + 1
  when :dn_right    then is_roll_at? x + 1, y + 1
  end
end

@remove = []

def mark_remove x, y
  @remove << [x, y]
end

def remove_rolls
  @remove.each do |xy|
    x = xy[0]
    y = xy[1]
    @data[y][x] = 'x'
  end
  @remove = []
end

def pt1
  copy_data @data1
  find_dimensions
  puts "@xsize: #{@xsize}"
  puts "@ysize: #{@ysize}"

  0.upto(@ysize) do |y|
    0.upto(@xsize) do |x|
      border = 0
      if is_roll_at? x, y
        border += 1 if is_roll?(:up_left  , x, y)
        border += 1 if is_roll?(:up_centre, x, y)
        border += 1 if is_roll?(:up_right , x, y)
        border += 1 if is_roll?(:left     , x, y)
        border += 1 if is_roll?(:right    , x, y)
        border += 1 if is_roll?(:dn_left  , x, y)
        border += 1 if is_roll?(:dn_centre, x, y)
        border += 1 if is_roll?(:dn_right , x, y)
        @total += 1 if (border < 4)
      end
    end
  end
end

def pt2
  iter = 0
  copy_data @data1
  find_dimensions
  puts "@xsize: #{@xsize}"
  puts "@ysize: #{@ysize}"

  done = false
  while ! done
    0.upto(@ysize) do |y|
      0.upto(@xsize) do |x|
        border = 0
        if is_roll_at? x, y
          border += 1 if is_roll?(:up_left  , x, y)
          border += 1 if is_roll?(:up_centre, x, y)
          border += 1 if is_roll?(:up_right , x, y)
          border += 1 if is_roll?(:left     , x, y)
          border += 1 if is_roll?(:right    , x, y)
          border += 1 if is_roll?(:dn_left  , x, y)
          border += 1 if is_roll?(:dn_centre, x, y)
          border += 1 if is_roll?(:dn_right , x, y)
          if (border < 4)
            @total += 1
            done = false
            mark_remove x, y
          end
        end
      end
    end
    done = true if @remove == []
    unless done
      remove_rolls
      iter += 1
      done = true if iter > 100
    end
  end
end

